# Loop through the list and create necessary folders and files
for file_path in list_of_files:
    path = Path(file_path)

    # Extract the directory and create it if it doesn't exist
    file_dir = path.parent
    if file_dir:
        os.makedirs(file_dir, exist_ok=True)  # Create folders if they don’t exist

    # Create the file if it does not exist
    if not path.exists():
        path.touch()  # Creates an empty file
        print(f"Created: {file_path}")
    else:
        print(f"Already exists: {file_path}")

